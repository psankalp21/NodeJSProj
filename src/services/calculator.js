function add(num1, num2) {
  const result = num1 + num2;
  return result;
}

function sub(num1, num2) {
  const result = num1 - num2;
  return result;
}

function mul(num1, num2) {
  const result = num1 * num2;
  return result;
}

function div(num1, num2) {
  const result = num1 / num2;
  return result;
}

module.exports = {
  add,
  sub,
  mul,
  div
};
